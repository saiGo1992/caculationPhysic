%双缝干涉%
ym=1.25;    %屏幕上观察点离中心最大距离
y=linspace(-ym,ym,101); %屏幕上取n个观察点
z=1000;     %屏幕到缝的距离
lambda=5e-4; %设置光的波长
for d=0.2:0.2:3
    L1=sqrt((y-d/2).^2+z^2);  %屏上一点到两个光源的距离
    L2=sqrt((y+d/2).^2+z^2);
    phi=2*pi*(L2-L1)/lambda;    %计算相位差
    I=4*(cos(phi/2)).^2;    %利用相位差计算光强
    subplot(2,1,1);     
    plot(y,I)%用曲线表示光强大小来代表干涉条纹
    axis([-1.25 1.25 0 4]);
    subplot(2,1,2);
    B=I*255/5;   %定标取255个级，I/5最亮
    image(B);   %以图案表示干涉条纹
    colormap(gray(255)); %用灰度级别显示图案
    pause(1);
end