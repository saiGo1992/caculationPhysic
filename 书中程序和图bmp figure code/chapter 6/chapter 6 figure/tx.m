% 画坐标系程序
function tx(xmin,xmax,ymin,ymax)
dx = (xmax-xmin)/15; dy = (ymax-ymin)/15;
plot([xmin xmax],[0 0],'k','Linewidth',1);      % 画x轴
hold on;
plot([0 0],[ymin ymax],'k','Linewidth',1);      % 画y轴
AXIS(AXIS);
ARROW([xmax  0],[xmax+dx 0]); % 画x轴箭头
ARROW([0 ymax],[0 ymax+dy]);  % 画y轴箭头
xlabel_str = '$x$';
ylabel_str = '$t$';
xp = (-dx-xmin)/(xmax-xmin);
yp = (dy/2-ymin)/(ymax-ymin); % 注意xlabel的位置
text('string',xlabel_str,...
    'Interpreter','latex',...
    'FontSize',16,...
    'units','norm',...
    'position',[1 yp]);
text('string',ylabel_str,...
    'Interpreter','latex',...
    'FontSize',16,...
    'units','norm',...
    'position',[xp 1]);
axis('square');
axis off;


